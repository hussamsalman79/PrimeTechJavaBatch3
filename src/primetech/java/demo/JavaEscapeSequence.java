package primetech.java.demo;

public class JavaEscapeSequence {
	public static void main(String[] args) {
//	Printing with /n for escape sequence 
		System.out.print("My name is Sam \nMy age is 43");
		
//	Printing with /t for adding Horizontal tab 
		System.out.println("\n\tI would like to .......");
		
//	Printing with backslash \\ will print 1 single backslash
		
		System.out.println("\tHello \\ my name is John");
		
//	Printing double quotes " as a sting 
//	I like to the world "java" very much 
		System.out.println("\tI like to the world \"java\" very much");
			
	}

}
