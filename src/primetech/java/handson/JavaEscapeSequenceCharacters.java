package primetech.java.handson;

public class JavaEscapeSequenceCharacters {
	public static void main(String[] args) {
		// print hello world with new line using \n
		System.out.println("\n Hello World" );
		
		//print your pet name that starts with tab \t
		System.out.println("\t my dog name is toby");
		
		//Print a sentence that has a word  which surrounded by ""
		
		System.out.println("This is Husam I go by \"Sam\"" );
		// print a sentence that has \ in it  
		
		System.out.println("I love \\VA\\");
		
		
	}

}
