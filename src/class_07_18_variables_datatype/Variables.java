package class_07_18_variables_datatype;

public class Variables {
	
	public static void main(String[] args) {
		
		byte age;
		age = 40;  
		System.out.println(age);
		
		boolean mb ;
		mb = false; 
		System.out.println(mb);				
		
		double rate; 
		rate = 1.5;	
		System.out.println(rate);
		
		short smallNumber; 
		smallNumber = 13; 
		System.out.println(smallNumber);
		
		char firstintail; 
		firstintail= 'S';
		System.out.println(firstintail);
		
		long bigNumber; 
		bigNumber= 234567899999l; 
		System.out.println(bigNumber);
		
		int Number; 
		Number= 100;  
		System.out.println(Number);
		
		float fnumber; 
		fnumber =  4.2f; 
		System.out.println(fnumber);
		
		String myName; 
		myName = "Husam"; 
		
		System.out.println(myName);
		
		
		int price =  2; 
		int quantity = 10; 
		int total; 
		
		total = price * quantity; 
		
		System.out.println(total);
	
	}

}
