package class_08_17_loops;


public class HandsOn_for_loop {
	public static void main(String[] args) {
		
		
		// Print odd numbers between 1-100
		 for (int i=1; i<=100; i++) {
			 if (i%2 ==1) {
				 System.out.println(i);			 
			 }	 
		// Print even numbers between 1-100	 
			 for (int x = 0; x<=100; x++) {	 
				 if (x%2==0) {
					 System.out.println(x);
				 }
			 }
			 
		 }
		
	}

}
